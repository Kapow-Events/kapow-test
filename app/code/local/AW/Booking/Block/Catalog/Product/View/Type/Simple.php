/**
 * Simple product data view
 *
 * @category   Mage
 * @package    Mage_Catalog
 * @author      Magento Core Team <core@magentocommerce.com>
 */

class AW_Booking_Block_Catalog_Product_View_Type_Simple extends Mage_Catalog_Block_Product_View_Type_Simple
{
    public function __construct()
    {
        parent::__construct();
        if (preg_match('/^(1.(3.[0-9].[0-9]|4.[0-1].[0-9]|[7-8].[0-9].[0-9]))/', Mage::getVersion()))
            $this->setTemplate('catalog/product/view/type/simple.phtml');
        else
            $this->setTemplate('catalog/product/view/type/default.phtml');
    }
}
